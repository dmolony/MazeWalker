package com.bytezone.wizardry.origin;

// -----------------------------------------------------------------------------------//
public class Location
// -----------------------------------------------------------------------------------//
{
  int level;
  int row;
  int column;

  // ---------------------------------------------------------------------------------//
  public Location (int level, int column, int row)
  // ---------------------------------------------------------------------------------//
  {
    this.level = level;
    this.row = row;
    this.column = column;
  }

  // ---------------------------------------------------------------------------------//
  @Override
  public String toString ()
  // ---------------------------------------------------------------------------------//
  {
    return String.format ("%2d %2d %2d", level, column, row);
  }
}
